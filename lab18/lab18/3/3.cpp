#include <iostream>  // Подключение стандартной библиотеки ввода-вывода
int sum(int n, ...); // Прототип функции sum для работы с переменным числом аргументов
using namespace std;

void main()
{
    // Вызываем функцию sum с шестью аргументами: 4, 5, 1, 2, 3, 0
    // Первый аргумент (6) указывает количество последующих чисел
    cout << sum(6, 4, 5, 1, 2, 3, 0) << std::endl;

    // Вызываем функцию sum с тремя аргументами: 34, 4445
    // Первый аргумент (2) указывает количество последующих чисел
    cout << sum(2, 34, 4445);
}

// Функция sum: вычисляет сумму чисел, переданных после первого аргумента
int sum(int n, ...)
{
    int* p = &n;  // Указатель на первый аргумент (n)
    int sum = 0;  // Переменная для накопления суммы

    // Цикл для обработки n чисел, переданных после первого аргумента
    for (int i = 1; i <= n; i++)
        sum += *(++p);  // Переходим к следующему аргументу и добавляем его к сумме

    return sum;  // Возвращаем сумму
}
