#include <iostream>  // Подключение стандартной библиотеки ввода-вывода
using namespace std;

// Функция f1: выводит сообщение и возвращает значение 1
int f1(const char* s)
{
    cout << s << " \nВыполняется тест №1" << endl;  // Вывод строки и сообщения о выполнении теста
    return 1;  // Возврат значения 1
}

// Функция f2: выводит сообщение и возвращает значение 2
int f2(const char* s)
{
    cout << s << " \nВыполняется тест №2" << endl;  // Вывод строки и сообщения о выполнении теста
    return 2;  // Возврат значения 2
}

// Функция callfun: вызывает переданную в неё функцию через указатель
// Параметры:
//   s - строка, которая будет передана в вызываемую функцию
//   f - указатель на функцию, принимающую const char* и возвращающую int
void callfun(const char* s, int (*f)(const char*))
{
    // Вызов переданной функции f с передачей строки s, и вывод результата
    cout << " f() = " << f(s) << '\n';
}

// Главная функция программы
int main()
{
    setlocale(0, "Rus");  // Установка локализации для корректного отображения кириллицы

    // Вызов функции callfun с передачей строки и указателя на функцию f1
    callfun("Тест №1 вызван", f1);

    // Вызов функции callfun с передачей строки и указателя на функцию f2
    callfun("Тест №2 вызван", f2);

    return 0;  // Успешное завершение программы
}
